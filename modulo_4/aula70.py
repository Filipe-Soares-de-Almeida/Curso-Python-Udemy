"""
Retorno de funções em Python
"""

def soma(a, b):
  """
  Soma dois números inteiros e retorna o resultado.

  Parâmetros:
  a (int): O primeiro número a ser somado.
  b (int): O segundo número a ser somado.

  Retorna:
  int: O resultado da soma de a e b.
  """
  return a + b

soma1 = soma(1, 2)
soma2 = soma(5, 5)

print(soma1)
print(soma2)

