# operadores matematicos

# soma +
soma = 2 + 4
print('Soma', soma);

# subtracao -
subtracao = 2 - 4
print('Subtracao', subtracao);

# multiplicacao *
multiplicacao = 2 * 4
print('Multiplicacao', multiplicacao);

# divisao /
divisao = 2 / 4
print('Divisao', divisao);

# divisao inteira //
divisao_inteira = 2 // 4
print('Divisao inteira', divisao_inteira);

# modulo %
modulo = 2 % 4
print('Modulo', modulo);

# potencia **
potencia = 2 ** 4
print('Potencia', potencia);