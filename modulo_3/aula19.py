# Operadores de comparação (relacionais)
# <, >, <=, >=, ==, !=

# < - menor que
# > - maior que
# <= - menor ou igual
# >= - maior ou igual
# == - igual
# != - diferente

maior = 1 > 2
menor = 1 < 2
maior_ou_igual = 1 >= 2
menor_ou_igual = 1 <= 2
igual = 1 == 2
diferente = 1 != 2

print(f'{maior=}')
print(f'{menor=}')
print(f'{maior_ou_igual=}')
print(f'{menor_ou_igual=}')
print(f'{igual=}')
print(f'{diferente=}')