# tipo de dado bool (boolean)
# só possui dois valores: True ou False
# é validado com o operador de igualdade == ou diferente !=
# tambem é validado com os operadores matematicos > e <

print("Somente True  ", " - " , True )
print("Somente False ", " - ", False)
print("True == False ", " - ", True == False,)
print("True != False ", " - ", True != False)
print("0 == False    ", " - ",  0 == False, )
print("0 != False    ", " - ", 0 != False)
print("1 > 2         ", " - ", 1 > 2)
print("1 < 2         ", " - ", 1 < 2)
print("1 == 1        ", " - ", 1 == 1)

