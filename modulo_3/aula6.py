# conversao de tipos, coerção, type convertion, typecasting, coercion
# é uma funcao que transforma um tipo de dado em outro

# int() - converte para int
# float() - converte para float
# str() - converte para str
# bool() - converte para bool

print(int(12.5))
print(float(12))
print(str(12.5))
print(bool(12.5))
print(bool(0))